# 数字同士の足し算
print( 1 + 2 )
print()

# 文字同士の足し算
print("1"+"2")
print()

# 引き算（その１）
print( 3 - 2 )
print()

# 引き算（その２）
print( 0.3 - 0.2 )
print()

# 数字同士の掛け算
print( 2 * 3 )
print()

# 文字と数字の掛け算
print( "2" * 3 )
print()

# 数字と文字の掛け算
print( 2 * "3" )
print()

# 割り算
print( 23 / 4 )
print()

# 整数除算（切り捨て）
print( 23 // 4 )
print()

# 剰余算（あまり）
print( 23 % 4 )
print()